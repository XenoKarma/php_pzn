<?php

echo "Tipe Data Number\n";

echo "decimal: ";
var_dump(1234);
//tipenya integer

echo "octal: ";
var_dump(0123);
//untuk octal, angka diawali dengan 0
//tipenya integer

echo "hexadecimal: ";
var_dump(0x1A);
//untuk hexadecimal, angka diawali dengan 0x
//tipenya integer

echo "binary: ";
var_dump(0b11111111);
//untuk binary, angka diawali dengan 0b
//tipenya integer

echo "floating point: ";
var_dump(1.234);
//tipenya float

echo "floating point: ";
var_dump(1.2e3);
//1.2e3 artinya 1.2 * 10^3
//tipenya float

echo "floating point: ";
var_dump(7.2E-10);
//7.2E-10 artinya 7.2 * 10^-10
//tipenya float
